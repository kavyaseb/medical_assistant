# ── Urgency calculation ────────────────────────────────────────────────────────

def calculate_urgency(score: int) -> dict:
    """
    Convert a numeric urgency score (1–100) into a human-readable triage level.

    Args:
        score: Integer between 1 and 100 representing symptom urgency.

    Returns:
        dict with keys: score, level, label, recommendation, disclaimer
    """
    score = max(1, min(100, int(score)))  # clamp to [1, 100]

    if score <= 25:
        level = "LOW"
        label = "Low Urgency"
        recommendation = (
            "Your symptoms appear mild. Consider self-care measures such as rest, "
            "hydration, and over-the-counter remedies if appropriate. Monitor your "
            "symptoms and contact a healthcare provider if they worsen or persist "
            "beyond a few days."
        )
    elif score <= 50:
        level = "MEDIUM"
        label = "Medium Urgency"
        recommendation = (
            "Your symptoms warrant medical attention but are not immediately life-threatening. "
            "Schedule an appointment with your primary care provider within 1–3 days. "
            "If symptoms worsen significantly before then, seek urgent care."
        )
    elif score <= 75:
        level = "HIGH"
        label = "High Urgency"
        recommendation = (
            "Your symptoms suggest you should be evaluated today. Please visit an urgent "
            "care clinic or contact your doctor for a same-day appointment. Do not wait "
            "if symptoms are getting worse."
        )
    else:
        level = "EMERGENCY"
        label = "EMERGENCY — Seek Immediate Care"
        recommendation = (
            "⚠️  CALL 911 or go to the nearest Emergency Room IMMEDIATELY. "
            "Your symptoms may indicate a serious or life-threatening condition "
            "that requires immediate professional medical attention. Do not drive yourself."
        )

    return {
        "score": score,
        "level": level,
        "label": label,
        "recommendation": recommendation,
        "disclaimer": (
            "EDUCATIONAL DISCLAIMER: This urgency level is generated by an AI tool "
            "for educational purposes only and does not constitute medical advice or "
            "a clinical triage assessment. Always consult a qualified healthcare "
            "professional for medical concerns."
        ),
    }


# ── Report generation ──────────────────────────────────────────────────────────

def generate_report(
    primary_symptom: str,
    symptom_duration: str,
    severity_score: int,
    associated_symptoms: list[str],
    red_flags_present: list[str],
    urgency_level: str,
    urgency_score: int,
    recommendation: str,
    age_group: str = "Not specified",
    relevant_history: str = "None reported",
    additional_notes: str = "",
) -> dict:
    """
    Assemble a structured JSON triage report from collected assessment data.

    Returns:
        dict representing the full triage report.
    """
    report = {
        "report_metadata": {
            "tool": "Medical Assistant — Educational Symptom Assessment",
            "generated_at": datetime.utcnow().isoformat() + "Z",
            "disclaimer": (
                "THIS REPORT IS FOR EDUCATIONAL PURPOSES ONLY. It does not constitute "
                "medical advice, a clinical diagnosis, or a substitute for evaluation by "
                "a licensed healthcare professional. In an emergency, call 911 immediately."
            ),
        },
        "patient_summary": {
            "age_group": age_group,
            "relevant_medical_history": relevant_history,
        },
        "symptom_assessment": {
            "primary_symptom": primary_symptom,
            "duration": symptom_duration,
            "self_reported_severity": f"{severity_score}/10",
            "associated_symptoms": associated_symptoms,
            "red_flags_identified": red_flags_present if red_flags_present else ["None identified"],
        },
        "triage_result": {
            "urgency_score": urgency_score,
            "urgency_level": urgency_level,
            "recommendation": recommendation,
        },
        "additional_notes": additional_notes or "None",
        "follow_up_reminders": [
            "Keep track of any new or worsening symptoms.",
            "Bring this report summary to your healthcare provider if helpful.",
            "This assessment does not replace a clinical examination.",
        ],
    }
    return report


