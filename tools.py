# ── Tool dispatcher ────────────────────────────────────────────────────────────

def run_tool(tool_name: str, tool_input: dict) -> str:
    """
    Dispatch a tool call by name and return the result as a JSON string.

    Args:
        tool_name:  Name of the tool to run.
        tool_input: Dictionary of arguments from the model.

    Returns:
        JSON-encoded string of the tool result.
    """
    if tool_name == "calculate_urgency":
        result = calculate_urgency(score=tool_input["score"])
    elif tool_name == "generate_report":
        result = generate_report(**tool_input)
    else:
        result = {"error": f"Unknown tool: {tool_name}"}

    return json.dumps(result, indent=2)
